public class Bolton_Army {

    private int soldiers;
    private int horsemen;
    private int elephantmen;
    private int knights;

    public int getSoldiers() {
        return soldiers;
    }

    public void setSoldiers(int soldiers) {
        this.soldiers = soldiers;
    }

    public int getHorsemen() {
        return horsemen;
    }

    public void setHorsemen(int horsemen) {
        this.horsemen = horsemen;
    }

    public int getElephantmen() {
        return elephantmen;
    }

    public void setElephantmen(int elephantmen) {
        this.elephantmen = elephantmen;
    }

    public int getKnights() {
        return knights;
    }

    public void setKnights(int knights) {
        this.knights = knights;
    }
}
